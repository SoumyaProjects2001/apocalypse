$(document).ready(function(){
  $(".F1").click(function(){
    $(".para-1").removeClass("None");
    $(".FF1").removeClass("none");
    $(".para-1").addClass("OPACITY");
    $(".FF1").addClass("OPACITY-B");
  });
});

$(document).ready(function(){
  $(".FF1").click(function(){
    $(".para-1").addClass("None");
    $(".FF1").removeClass("OPACITY-B");
    $(".FF1").addClass("none");
    $(".para-1").removeClass("OPACITY");
  });
});

